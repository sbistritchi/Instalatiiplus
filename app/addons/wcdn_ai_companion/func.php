function fn_ai_companion_generate_product_request($product_id, $template)
{
	$auth = &Tygh::$app['session']['auth'];
	$product_data = fn_get_product_data($product_id, $auth, DESCR_SL, '', true, true, true, true, false, false, true);
	

	$fields_to_generate = [];
	$fields_to_include = [];
	
	foreach($template['fields_to_include'] as $field => $include)
	{
		if($include == "Y")
		{
			$fields_to_include[] = $field;
		}	
	}
	
	foreach($template['fields_to_generate'] as $field => $include)
	{
		if($include == "Y")
		{
			$fields_to_generate[] = $field;
		}	
	}
	
	$product_fields = $needed_fields = [];
	
	if($product_data && !empty($fields_to_generate) && !empty($fields_to_include))
	{
		foreach($fields_to_include as $field)
		{
			$product_fields[$field] = $product_data[$field];
		}
		
		
		foreach($fields_to_generate as $field)
		{
			$needed_fields[$field] = '';
		}
		
		$image_path = '';
		if(in_array('main_pair', $fields_to_include) && isset($product_fields['main_pair']))
		{
			$image_path = $product_fields['main_pair']['detailed']['image_path'];
			unset($product_fields['main_pair']);
		}
		
		if(in_array('features', $fields_to_include))
		{
			$product_fields['features'] = [];
			foreach($product_data['product_features'] as $feature)
			{
				$product_fields['features'][$feature['description']] = $feature['variant'];
			}
			
			$product_fields['features'] = array_filter($product_fields['features']);
		}
		
		$needed_fields['score'] = '';
		$request = $template['prompt'] .' Datele produsului: '. json_encode($product_fields) .'. Trebuie sa returnezi: '. json_encode($needed_fields);

		$content_data = 
		[
			'product_id' 		=> $product_id,
			'template_id' 		=> $template['template_id'],
			'companion_model' 	=> $template['companion_model'],
			'request' 			=> $request,
			'image_path'		=> (!empty($image_path)) ? $image_path : '',
			'response'			=> '',
			'status' 			=> 'P',
			'applied'			=> 'N',
			'score'				=> 0,
			'timestamp'			=> time(),
		];

		
		db_query("INSERT INTO ?:ai_content ?e", $content_data);
	}
}

	function fn_ai_companion_get_content($content_id)
	{
		$openai_api_key = Registry::get('addons.wcdn_ai_companion.openai_api_key');
		if(empty($openai_api_key))
		{
			return false;
		}
		
		$content_data = db_get_row("SELECT * FROM ?:ai_content WHERE content_id = ?i", $content_id);
		if(empty($content_data))
		{
			return false;
		}
		
		$template_data = db_get_row("SELECT * FROM ?:ai_templates WHERE template_id = ?i", $content_data['template_id']);
		
		$url = 'https://api.openai.com/v1/chat/completions';

		$headers = 
		[
			'Content-Type: application/json',
			'Authorization: Bearer ' . $openai_api_key
		];
		
		

		$data = 
		[
			'model' => $content_data['companion_model'],
			'messages' => 
			[
				[
					'role' => 'system',
					'content' => 'Vei procesa ce iti cer eu, primesti datele in format JSON, si imi returnezi datele tot in format JSON fara alte texte pe langa, dupa modelul pe care ti-l dau. Vei genera un scor SEO bazat pe keyword-ul principal al produsului, de la 1 la 100, folosind cunostintele tale de SEO (pentru google)'
				],
				[
					'role' => 'user',
					'content' => 
					[
						[
							'type' => 'text',
							'text' => $content_data['request'],
						],
					]
				]
			],
		];
		
		if(!empty($content_data['image_path']))
		{
			$data['messages'][1]['content'][] = 
			[
				'type' => 'image_url',
            	'image_url' => 
            	[
            		"url" => $content_data['image_path'],
            	]
			];
		}
		

		if($template_data['temperature'] > 0)
		{
			$data['temperature'] = (float)$template_data['temperature'];
		}
		
		if($template_data['max_tokens'] > 0)
		{
			$data['max_tokens'] = $template_data['max_tokens'];
		}
		
		if($template_data['top_p'] > 0)
		{
			$data['top_p'] = (float)$template_data['top_p'];
		}
		
		//todo assistant API
/* 
		
		if(!empty($template_data['assistant_id']))
		{
			$data['assistant_id'] = $template_data['assistant_id'];
		}
 */
		
		//error_log(print_r($data, true), 3, 'chatGPT request');

		$options = 
		[
			CURLOPT_URL 			=> $url,
			CURLOPT_RETURNTRANSFER 	=> true,
			CURLOPT_POST 			=> true,
			CURLOPT_HTTPHEADER 		=> $headers,
			CURLOPT_POSTFIELDS 		=> json_encode($data)
		];

    	$request = curl_init();
   		curl_setopt_array($request, $options);
   		
		$response = curl_exec($request);
		//error_log(print_r($response, true), 3, 'chatGPT response');
		$http_code = curl_getinfo($request, CURLINFO_HTTP_CODE);
		curl_close($request);
		

		if($http_code === 200) 
		{
			$decoded_response = json_decode($response, true);
			return $decoded_response['choices'][0]['message']['content'] ?? false;
		} 
		else 
		{
			db_query("UPDATE ?:ai_content SET status = ?s, response = ?s, score = ?i WHERE content_id = ?i", 'E', $response, 0, $content_data['content_id']);
			return false;
		}
	}
